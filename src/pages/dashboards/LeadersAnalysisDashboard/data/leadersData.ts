// Generate mock sentiment trend data for last 30 days
export function generateSentimentTrendData(days: number = 30): SentimentTrendData[] {
  const data: SentimentTrendData[] = [];
  const today = new Date();

  for (let i = days - 1; i >= 0; i--) {
    const date = new Date(today);
    date.setDate(date.getDate() - i);

    // Generate somewhat realistic fluctuating sentiment values
    const baseNoise = Math.sin(i * 0.3) * 0.1;

    data.push({
      date: date.toISOString().split('T')[0],
      bjp_sentiment: 0.35 + baseNoise + (Math.random() - 0.5) * 0.15,
      tmc_sentiment: 0.45 + baseNoise + (Math.random() - 0.5) * 0.12,
      inc_sentiment: 0.18 + (Math.random() - 0.5) * 0.1,
      overall_sentiment: 0.38 + baseNoise + (Math.random() - 0.5) * 0.1,
    });
  }

  return data;
}

